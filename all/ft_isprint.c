#include "libft.h"

int	ft_isprint(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 0 && str[i] < 32)
			return (0);
		else if (str[i] == 127)
			return (0);
		i++;
	}
	return (1);
}
